N, M = map(int, input().split())
arr = [list(map(int, input().split())) for _ in range(N)]
ret = 0

shapes = [[[0, 1], [0, 2], [0, 3]], # ㅡ
         [[1, 0], [2, 0], [3, 0]], # ㅣ
         [[0, 1], [1, 0], [1, 1]], 
         [[1, 0], [2, 0], [2, 1]],
         [[1, 0], [2, 0], [2, -1]],
         [[0, 1], [0, 2], [1, 0]],
         [[0, 1], [0, 2], [1, 2]],
         [[0, 1], [1, 1], [2, 1]],
         [[0, 1], [1, 0], [2, 0]],
         [[0, 1], [0, 2], [-1, 2]],
         [[1, 0], [1, 1], [1, 2]],
         [[1, 0], [1, 1], [2, 1]],
         [[1, 0], [1, -1], [2, -1]],
         [[0, 1], [-1, 1], [-1, 2]],
         [[0, 1], [1, 1], [1, 2]],
         [[0, 1], [0, 2], [1, 1]],
         [[1, 0], [1, 1], [2, 0]],
         [[1, 0], [1, -1], [2, 0]],
         [[0, 1], [0, 2], [-1, 1]]]

for i in range(N):
    for j in range(M):
        for shape in shapes:
            sum = arr[i][j]
            
            for k in range(3):
                y = i + shape[k][0]
                x = j + shape[k][1]

                if 0 <= x <= M-1 and 0 <= y <= N-1:
                    sum += arr[y][x]
            
            ret = max(ret, sum)

print(ret)